    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        int selectedOption = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus semua data logistik?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (selectedOption == JOptionPane.YES_OPTION) {
            boolean berhasil = controllerLogistik.deleteLogistik();

            if (berhasil) {
                JOptionPane.showMessageDialog(this, "Berhasil menghapus semua data logistik.");
                refreshTable();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data logistik.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

