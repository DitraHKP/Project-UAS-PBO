        private void refreshTable() {
        List<Logistik> logistikList = controllerLogistik.getAllLogistik();
        DefaultTableModel tableModel = (DefaultTableModel) tableLogistik.getModel();
        tableModel.setRowCount(0); // Clear existing data

        for (Logistik logistik : logistikList) {
            tableModel.addRow(new Object[]{logistik.getNama(), logistik.getJumlah()});
        }
    }

